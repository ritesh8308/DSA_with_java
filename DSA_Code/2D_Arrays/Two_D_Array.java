public class Two_D_Array {
    public static void main(String args[]) {

int[][] marks = new int[3][3]; 
     
    for (int i=0; i<3; i++) {
        
        for (int j=0; j<3; j++) {
            marks[i][j] = 1;
            System.out.print(marks[i][j]+" "); 
        }
        System.out.println(); 
    }

    
    }
}